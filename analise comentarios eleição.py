def contar_ocorrencias(texto):
    # Dividindo o texto por falas, uma linha para cada fala
    falas = texto.strip().split("\n\n")
    
    # Dicionário para contar quantas vezes "1" e "2" apareceram
    contagem = {'1': 0, '2': 0}
    
    # Dicionário para armazenar quais pessoas já falaram "1" ou "2"
    pessoas_ja_contaram = {}

    for fala in falas:
        # A fala pode ser separada em nome da pessoa e o que ela disse
        partes = fala.split("\n", 1)
        
        if len(partes) < 2:
            continue  # pula se a fala não tiver nome e conteúdo
        
        pessoa, conteudo = partes[0], partes[1].lower()
        
        # Inicializando o contador de cada pessoa caso não exista
        if pessoa not in pessoas_ja_contaram:
            pessoas_ja_contaram[pessoa] = {'1': False, '2': False}
        
        # Verificando se a pessoa mencionou "1" ou "2" e contando se for a primeira vez
        if '1' in conteudo and not pessoas_ja_contaram[pessoa]['1']:
            contagem['1'] += 1
            pessoas_ja_contaram[pessoa]['1'] = True
        
        if '2' in conteudo and not pessoas_ja_contaram[pessoa]['2']:
            contagem['2'] += 1
            pessoas_ja_contaram[pessoa]['2'] = True

    return contagem

# Chamando a função e mostrando os resultados
resultado = contar_ocorrencias(texto)
print(f'Contagem de "1": {resultado["1"]}')
print(f'Contagem de "2": {resultado["2"]}')