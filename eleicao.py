def contar_chapa_1_e_2(texto):
    # Dicionário para contar as ocorrências de "chapa 1" e "chapa 2"
    contagem = {'chapa 1': 0, 'chapa 2': 0}
    
    # Dividir o texto em palavras ou grupos de palavras
    palavras = texto.split()

    # Iterar pelas palavras e verificar as sequências "chapa 1" e "chapa 2"
    for i in range(len(palavras) - 1):
        # Verifica se a sequência "chapa 1" está presente
        if palavras[i] == "chapa" and palavras[i+1] == "1":
            contagem['chapa 1'] += 1
        # Verifica se a sequência "chapa 2" está presente
        elif palavras[i] == "chapa" and palavras[i+1] == "2":
            contagem['chapa 2'] += 1
    
    return contagem

